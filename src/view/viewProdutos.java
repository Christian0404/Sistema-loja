    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
            Produto p = new Produto();
            ProdutoDAO dao = new ProdutoDAO();
            
            p.setProduto(txtDescricao.getText());
            p.setTamanho(Integer.parseInt(txtQuantidade.getText()));
            p.setVenda(Float.parseFloat(txtPreco.getText()));
            p.setUnidade(txtUnidade.getText());
            dao.create(p);
            
            txtDescricao.setText("");
            txtQuantidade.setText("");
            txtPreco.setText("");
            txtUnidade.setText("");
            
            readJtable();
    }//GEN-LAST:event_jToggleButton1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            //ATUALIZA
            if(jTProdutos.getSelectedRow() != -1){
            
            Produto p = new Produto();
            ProdutoDAO dao = new ProdutoDAO();
            
            p.setProduto(txtDescricao.getText());
            p.setTamanho(Integer.parseInt(txtQuantidade.getText()));
            p.setVenda(Float.parseFloat(txtPreco.getText()));
            p.setUnidade(txtUnidade.getText());
            p.setId((int) jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 4));
            dao.update(p);
            
            txtDescricao.setText("");
            txtQuantidade.setText("");
            txtPreco.setText("");
            txtUnidade.setText("");
            
            readJtable();
            }
    }//GEN-LAST:event_jButton1ActionPerformed

