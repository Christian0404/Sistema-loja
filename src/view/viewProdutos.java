    private void jTProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTProdutosMouseClicked
        if(jTProdutos.getSelectedRow() != -1){
            txtDescricao.setText(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 0).toString());
            txtQuantidade.setText(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 1).toString());
            txtUnidade.setText(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 2).toString());
            txtPreco.setText(jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 3).toString());
        }
            
    }//GEN-LAST:event_jTProdutosMouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        
            if(jTProdutos.getSelectedRow() != -1){
            
            Produto p = new Produto();
            ProdutoDAO dao = new ProdutoDAO();
            
            p.setId((int) jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 4));
            dao.delete(p);
            
            txtDescricao.setText("");
            txtQuantidade.setText("");
            txtPreco.setText("");
            txtUnidade.setText("");
            
            readJtable();
            }else{
                JOptionPane.showMessageDialog(null,"Selecione um para excluir!");
            }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed


